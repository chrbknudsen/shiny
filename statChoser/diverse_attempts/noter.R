🧠 Forslag: Supplerende tests og grene
🟠 1. Non-parametriske alternativer – differentiering
Du har fx:
  
  "Use a non-parametric test (e.g., Wilcoxon)" ← det er lidt vagt

Men du kunne med fordel skelne eksplicit mellem:
  
  Wilcoxon signed-rank test (paired)

Mann-Whitney U (unpaired)

Kruskal-Wallis (1-faktor, flere grupper)

Friedman test (gentagne målinger, non-param.)

➡️ Disse nævnes delvist, men ikke klart adskilt i ruten (afhængig af hvordan du har sat spørgsmåls-id'er op)

🟠 2. Effect size og power
Beslutningstræet leder til en test, men:

Skal man også tage højde for effektstørrelse?

Skal man estimere required sample size?

➡️ Ikke tests i sig selv, men praktisk meget vigtige elementer, som måske bør nævnes.

🟠 3. Bayesianske alternativer
I stigende grad brugt i praksis, fx:

Bayesian t-test

Bayes factor ANOVA

Bayesian regression

➡️ Ikke nødvendigvis en erstatning, men kunne nævnes som alternativ i linket eller anbefalingsteksten.

🟠 4. Generaliserede mixede modeller (GLMM)
Ved fx:

gentagne målinger (hvor ANOVA antagelser ikke holder)

nested data (elever i klasser, patienter i hospitaler)

➡️ Kunne være alternativ/tilføjelse ved repeated measures eller regression.

🟠 5. Multivariate ikke-lineære modeller
GAM (Generalized Additive Models)

Non-linear regression

Spline-modeller

➡️ Kunne være en udvidelse af regressionsgrene.